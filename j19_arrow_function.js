// normal arrow function
hello = () => {
    console.log("Hello");
}

hello();

const sum = (a,b) => {
    return a+b;
}

console.log("sum of 4 and 5 is : ",sum(4,5));

let avg = (a,b) =>{
    console.log("average is : ",(a+b)/2);
}

avg(6,7);

sumThree = (a,b,c) =>{
    return a+b+c;
}

console.log("sum of 1,2,3 is ",sumThree(1,2,3));

