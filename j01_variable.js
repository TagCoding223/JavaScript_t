// JavaScript is dynamic typed language (change type of any variable at runtime using var)
// static typed language(first define type of variable then perform operation like c language)
// let & var use to create variable , const use to create constant
// try to avoid use of var (because its allow to change type of variable at run time at anywhere because its scoped globally)

// Declaring variable
var a="vishal";
console.log(a);

// change type at runtime
a=89;
console.log(a);

let b=67;
console.log(b);

// change type at runtime
b="money";
console.log(b);

// variable start with $ , _ or letter
// variables are case sensitive means Vishal and vishal are different variables